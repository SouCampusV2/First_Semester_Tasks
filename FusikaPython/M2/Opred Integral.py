def integrate_rectangle_method(func, a, b, n):
    """
    Вычисление определенного интеграла методом центральных прямоугольников.

    :param func: Интегрируемая функция
    :param a: Нижняя граница отрезка интегрирования
    :param b: Верхняя граница отрезка интегрирования
    :param n: Количество кусочков, на которые разбивается отрезок интегрирования
    :return: Приближенное значение определенного интеграла
    """
    h = (b - a) / n
    integral_sum = 0

    for i in range(n):
        x = a + i * h + h / 2  # центральная точка кусочка
        integral_sum += func(x)

    result = integral_sum * h
    return result

# Пример использования:
import math

# Задаем функцию для тестирования (например, квадратичная функция)
def test_function(x):
    return x**2

# Задаем границы отрезка интегрирования и количество кусочков
a = 0
b = 1
n = 100

# Вычисляем значение определенного интеграла
result = integrate_rectangle_method(test_function, a, b, n)

# Выводим результат
print(f"Приближенное значение интеграла: {result}")
